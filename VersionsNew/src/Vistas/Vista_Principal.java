    private void btnCrearUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearUserActionPerformed
        if(validar()){
            Usuario user = new Usuario();
            String usuario = txtCrearUser.getText();
            String password = txtCrearPass.getText();
            String gender = getGender();
            user.setUser(usuario);
            user.setPassword(password);
            user.setGender(gender);
            try {
                servicio_usuario.create(Conexion.obtener(), user);
                crearAccDialog.setVisible(false);
                JOptionPane.showMessageDialog(null, "Se a creado el usuario con exito!");
                sesionDialog.setVisible(true);
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error al conectar a la Base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            }catch(ClassNotFoundException ex){
                JOptionPane.showMessageDialog(null, "Error clase no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos Incompletos o Mayores a 50 caracteres");
        }
    }//GEN-LAST:event_btnCrearUserActionPerformed

    private void initBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_initBtnActionPerformed
        if(validarAcc()){
            try {
                acc = servicio_usuario.getByUser(Conexion.obtener(), txtUser.getText());
                String password = txtPass.getText().toString();
                String dbPassword = acc.getPassword();
                if(password.equals(dbPassword)){
                    Progreso p = new Progreso(pBar, programa);
                    p.execute();
                    //sesionDialog.setVisible(false);
                    //JOptionPane.showMessageDialog(null, "Bienvenido "+acc.getUser());
                    //userLbl.setText(acc.getUser());
                    //this.setVisible(true);
                    //this.checkConnection();
                }else{
                    JOptionPane.showMessageDialog(null, "Contrase√±a Incorrecta");
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error al conectar a la Base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            }catch(ClassNotFoundException ex){
                JOptionPane.showMessageDialog(null, "Error clase no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
            }catch(NullPointerException ex){
                JOptionPane.showMessageDialog(null, "No existe este usuario", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos Incompletos o Mayores a 50 caracteres");
        }
    }//GEN-LAST:event_initBtnActionPerformed

