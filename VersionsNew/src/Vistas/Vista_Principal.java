    public boolean validar(){
        boolean validar;
        String usuario = txtCrearUser.getText();
        String password = txtCrearPass.getText();
        validar = (!usuario.equals("") && !password.equals(""))||(usuario.length()>50 || password.length()>50);
        return validar;
    }
    
